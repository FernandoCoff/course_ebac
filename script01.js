// STRINGS

let frase = "JavaScript é muito legal!"

// Comprimento da string
console.log(frase.length)

// Maiúsculas
console.log(frase.toUpperCase())

// Minúsculas
console.log(frase.toLowerCase())

// Encontrar posição da string 
console.log(frase.indexOf('muito'))

// Estrair pate de uma frase
console.log(frase.substring(0,10))

// Substituir uma parte da string
console.log(frase.replace('muito legal!', 'Incrível!'))


